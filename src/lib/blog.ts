export function getCategoryById(id: string): BlogCategory | undefined {
  return BLOG_CATEGORIES.find((category) => category.id === id);
}

export function getArticleBySlug(slug: string): BlogArticle | undefined {
  return BLOG_ARTICLES.find((article) => article.slug === slug);
}

export function getFeaturedArticle(): BlogArticle | undefined {
  return BLOG_ARTICLES.find((article) => article.featured);
}

/**
 * نص بحث مُطبَّع لمقال واحد — يجمع العنوان والملخّص واسم التصنيف
 * والكلمات المفتاحية بنص واحد مطبَّع (عبر normalizeArabicText)، جاهز
 * للمطابقة مع كلمات بحث المستخدم. يُستخدم في BlogExplorer.
 */
export function getArticleSearchText(article: BlogArticle): string {
  const category = getCategoryById(article.category);
  const parts = [
    article.title,
    article.excerpt,
    category?.label ?? "",
    ...(article.keywords ?? []),
  ];
  return normalizeArabicText(parts.join(" "));
}
